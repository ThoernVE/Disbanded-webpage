function Guild(name, faction, region, realm, profile_url, raid_rankings, raid_progression,){
    this.name = name;
    this.faction = faction;
    this.region = region;
    this.realm = realm;
    this.profile_url = profile_url;
    this.raid_rankings = raid_rankings;
    this.raid_progression = raid_progression;
}

function GetGuildStats(region, realm, name){
    return fetch(`https://raider.io/api/v1/guilds/profile?region=${region}&realm=${realm}&name=${name}&fields=raid_rankings%2Craid_progression`)
    .then(response => response.json())
    .then(guildData => {return new Guild(
        guildData.name,
        guildData.faction,
        guildData.region,
        guildData.realm,
        guildData.profile_url,
        guildData.raid_rankings,
        guildData.raid_progression
    )
})
.catch(error => console.error("error fetching guild data:", error));
}

function PrintGuildData(region, realm, name){
    GetGuildStats(region, realm, name).then(newGuildData => {
        document.getElementById("raid_clear_one").innerText = newGuildData.raid_progression['blackrock-depths'].summary;
        document.getElementById("raid_rank_one").innerText = newGuildData.raid_rankings['blackrock-depths'].heroic.world + " World, " + 
        newGuildData.raid_rankings['blackrock-depths'].heroic.region + " Region, " + 
        newGuildData.raid_rankings['blackrock-depths'].heroic.realm + " Realm.";
        document.getElementById("raid_clear_two").innerText = newGuildData.raid_progression['nerubar-palace'].summary;
        document.getElementById("raid_rank_two").innerText = newGuildData.raid_rankings['nerubar-palace'].heroic.world + " World, " + 
        newGuildData.raid_rankings['nerubar-palace'].heroic.region + " Region, " + 
        newGuildData.raid_rankings['nerubar-palace'].heroic.realm + " Realm.";
      })

}

